#include "monty.h"

/**
 * montyQueue-  switch to queue mode
 *
 *
 * @stack:      unused data
 * @tokens:     unused data
 * @line:       the line number we are processing
 * @Q:          true for QUEUE mode, false for STACK mode
 *
 * Return:      nothing
 *
 */

void montyQueue(stack_t **stack, char **tokens, unsigned int line, bool *Q)
{
	stack = stack;
	tokens = tokens;
	line = line;
	*Q = true;
}
