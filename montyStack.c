#include "monty.h"

/**
 * montyStack-  switch to stack mode
 *
 *
 * @stack:      unused data
 * @tokens:     unused data
 * @line:       the line number we are processing
 * @Q:          true for QUEUE mode, false for STACK mode
 *
 * Return:      nothing
 *
 */

void montyStack(stack_t **stack, char **tokens, unsigned int line, bool *Q)
{
	stack = stack;
	tokens = tokens;
	line = line;
	*Q = 0;
}
